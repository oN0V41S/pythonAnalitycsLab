# Estrutura de Condicional Simples
soma = 3
if soma > 0:
  print("maior do que zero")
else:
  print("número menor que zero")

# Estrutura de Condicional aninhada
soma = -2

if soma == 0:
  print("Soma igual a zero")
elif soma > 0:
  print("soma é maior que zero")
else:
  print("soma é menor que zero")